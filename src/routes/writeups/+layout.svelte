<script lang="ts">
    import type { PageProps } from './$types';
    import { page } from '$app/state';
    import { getWriteup, type WriteupInfo } from '$lib/writeups/writeups';
    import { setContext, type Snippet } from 'svelte';
    import WriteupContent from './WriteupContent.svelte';
    import MathJaxScript from '$lib/mathjax/MathJaxScript.svelte';

    let { params, data, children }: PageProps & { children: Snippet } = $props();

    let options = $state({
        swearing: false
    });

    let writeupId: string | undefined = $state();
    let writeupInfo: WriteupInfo | undefined = $derived(writeupId ? getWriteup(writeupId) : undefined);

    $effect(() => {
        writeupId = page.route.id?.substring(page.route.id.lastIndexOf("/") + 1);
    });

    setContext("options", options);
</script>

<svelte:head>
    <title>{writeupInfo ? writeupInfo.name : "Writeups"}</title>
</svelte:head>

{#if writeupInfo && writeupInfo.usesMathJax}
    <MathJaxScript />
{/if}

{#if writeupInfo}
    <WriteupContent 
        title={writeupInfo.name} 
        description={writeupInfo.description}
        dateCreated={writeupInfo.modified || writeupInfo?.created} 
        hasSwearing={writeupInfo.hasSwearing}
        bind:canSwear={options.swearing}>
        {@render children?.()}
    </WriteupContent>
{:else}
    {@render children?.()}
{/if}

